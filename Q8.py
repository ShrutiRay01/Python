# Write a python program that concatenates two strings and returns the result.

# Function to concatenate two strings
def concatenate_strings():
    # Taking the first string input from the user
    string1 = input("Enter the first string: ")
    
    # Taking the second string input from the user
    string2 = input("Enter the second string: ")
    
    # Concatenating the two strings
    result = string1 + string2
    
    # Printing the concatenated result
    print(f"The concatenated string is: {result}")

# Calling the function
concatenate_strings()
